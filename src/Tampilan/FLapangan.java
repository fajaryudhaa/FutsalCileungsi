    private void lapangan()
    {
       try {
            String sql="select * from lapangan order by kode desc";
            RsLpg = KoneksiDB.getRS(sql); 
            if (RsLpg.next()) {
                String Lpg = RsLpg.getString("kode").substring(3);
                String AN = "" + (Integer.parseInt(Lpg) + 1);
                String Nol = "";

                if(AN.length()==1)
                {Nol = "000";}
                else if(AN.length()==2)
                {Nol = "00";}
                else if(AN.length()==3)
                {Nol = "0";}
                else if(AN.length()==4)
                {Nol = "";}

               TKode.setText("KD-" + Nol + AN);
            } else {
               TKode.setText("KD-0001");
            }

           }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
           }
     }

    private void isiGrid() {
        delGrid(TTable);
        DefaultTableModel defGrid = (DefaultTableModel) TTable.getModel();
        RsLpg = KoneksiDB.getRS("select * from lapangan");
        try {
            while (RsLpg.next()) {
                String[] data = {RsLpg.getString("kode"), RsLpg.getString("nama"),
                                 RsLpg.getString("harga"),RsLpg.getString("hargambr")};
                defGrid.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FLapangan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void delGrid(JTable gg) {
        DefaultTableModel defGrid = (DefaultTableModel) gg.getModel();
        int brs = gg.getRowCount();
        for(int i=0;i<brs;i++) {
            defGrid.removeRow(0);
        }
    }
