    private void txtcariKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcariKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER){
        Object[] Baris ={"Kode","Nama Lapangan","Harga","Harga Member"}; 
           tabmode = new DefaultTableModel(null, Baris); 
            String cariitem=txtcari.getText(); 
            
             try { 
                  String sql = "SELECT * FROM lapangan where kode like '%"+cariitem+"%' or nama like"
                          + "'%"+cariitem+"%' order by kode asc"; 
                    RsLpg = KoneksiDB.getRS(sql); 
                      while (RsLpg.next()){
                           tabmode.addRow(new Object[]{
                            RsLpg.getString(1),
                            RsLpg.getString(2),
                            RsLpg.getString(3),
                            RsLpg.getString(4),
                            RsLpg.getString(5)
                            });
                      }
                      TTable.setModel(tabmode);
             } catch (Exception e) {
             lapangan();
             }
        }
    }//GEN-LAST:event_txtcariKeyPressed


