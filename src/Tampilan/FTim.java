    private void txtcariKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcariKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER){
            Object[] Baris ={"Id","Nama Team","Alamat","No Telepon","Status"};
            tabmode = new DefaultTableModel(null, Baris);
            String cariitem=txtcari.getText();

            try {
                String sql = "SELECT * FROM team where kode like '%"+cariitem+"%' or nama like"
                + "'%"+cariitem+"%' order by kode asc";
                RsMbr = KoneksiDB.getRS(sql);
                while (RsMbr.next()){
                    tabmode.addRow(new Object[]{
                        RsMbr.getString(1),
                        RsMbr.getString(2),
                        RsMbr.getString(3),
                        RsMbr.getString(4),
                        RsMbr.getString(5)
                    });
                }
                TTable2.setModel(tabmode);
            } catch (Exception e) {
                member();
            }
        }
    }//GEN-LAST:event_txtcariKeyPressed

