    private void TTable2TTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TTable2TTableMouseClicked
        int baris= TTable2.getSelectedRow();
        if (team.carimember(TTable2.getValueAt
            (TTable2.getSelectedRow(),0).toString())==true) {
        TMember2.setText(team.getKode());
        TNama2.setText(team.getNama());
        TAlamat2.setText(team.getAlamat());
        TTlp2.setText(team.getNotlp());
        if("Member".equals(TTable2.getValueAt(baris,4).toString())){
            mmbr2.setSelected(true);
        }else{
            biasa2.setSelected(true);
        }
        }
    }//GEN-LAST:event_TTable2TTableMouseClicked

    private void BSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BSimpanActionPerformed
        String status="";
        if  (mmbr2.isSelected())
        {   status="Member";
        }
        else if (biasa2.isSelected())
        {   status="Biasa";
        }
        trans="tambah";
        String[] plg = {TMember2.getText(), TNama2.getText(), TAlamat2.getText(),TTlp2.getText(),status};
        if(team.updatemember(trans, plg)>0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Data Tim Tersimpan");
            emptyText();
            isiGrid();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Terjadi kesalahan, Silahkan coba lagi", "Kesalahan",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
        member();
    }//GEN-LAST:event_BSimpanActionPerformed

