    private void member()
    {
       try {
            String sql="select * from  team order by kode desc";
            RsMbr = KoneksiDB.getRS(sql); 
            if (RsMbr.next()) {
                String team = RsMbr.getString("kode").substring(3);
                String AN = "" + (Integer.parseInt(team) + 1);
                String Nol = "";

                if(AN.length()==1)
                {Nol = "000";}
                else if(AN.length()==2)
                {Nol = "00";}
                else if(AN.length()==3)
                {Nol = "0";}
                else if(AN.length()==4)
                {Nol = "";}

               TMember2.setText("MB-" + Nol + AN);
            } else {
               TMember2.setText("MB-0001");
            }

           }catch(Exception ex){
           JOptionPane.showMessageDialog(null, ex);
           }
     }
    
    private void isiGrid() {
        delGrid(TTable2);
        DefaultTableModel defGrid = (DefaultTableModel) TTable2.getModel();
        RsMbr = KoneksiDB.getRS("select * from team");
        try {
            while (RsMbr.next()) {
                String[] data = {RsMbr.getString("kode"), RsMbr.getString("nama"),
                                 RsMbr.getString("alamat"),RsMbr.getString("notlp"),
                                RsMbr.getString("status")};
                defGrid.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FTim.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void delGrid(JTable gg) {
        DefaultTableModel defGrid = (DefaultTableModel) gg.getModel();
        int brs = gg.getRowCount();
        for(int i=0;i<brs;i++) {
            defGrid.removeRow(0);
        }
    }

